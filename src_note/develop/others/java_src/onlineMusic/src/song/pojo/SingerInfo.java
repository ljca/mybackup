package song.pojo;

public class SingerInfo{

  private Integer singerId;

  private String singerName;

  private Integer singerTypeId;

  private Integer singerLocationId;

  public void setSingerId(Integer singer_id){this.singerId=singer_id;}

  public Integer getSingerId(){return this.singerId;}

  public void setSingerName(String singer_name){this.singerName=singer_name;}

  public String getSingerName(){return this.singerName;}

  public void setSingerTypeId(Integer singer_type_id){this.singerTypeId=singer_type_id;}

  public Integer getSingerTypeId(){return this.singerTypeId;}

  public void setSingerLocationId(Integer singer_location_id){this.singerLocationId=singer_location_id;}

  public Integer getSingerLocationId(){return this.singerLocationId;}

}
